package ChronoTimer;
/**
 * The system printer for the ChronoTimer.
 * 
 * @author Matt
 */
public class Printer {

	public Printer() {}
	
	/**
	 * Prints a String to the console (paper tape).
	 * 
	 * @param msg - the String to be printed.
	 */
	protected static void print(String msg) {
		System.out.println(msg);	
	}
}
